package com.example.qtcbeans;

public class EVeiculo extends Envolvido {
	
	private int idEVeiculo;
	private String placa;
	private String cor;
	private String modelo;
	private String marca;
	
	public EVeiculo() {}
	
	public int getIdEVeiculo() {
		return idEVeiculo;
	}
	public void setIdEVeiculo(int idEVeiculo) {
		this.idEVeiculo = idEVeiculo;
	}
	public String getPlaca() {
		return placa;
	}
	public void setPlaca(String placa) {
		this.placa = placa;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	
	
	
}
