package com.example.qtcbeans;

public class Policial {

	private int idPolicial;
	private String nomePolicial;
	private String usuarioPolicial;
	private String senhaPolicial;
	
	public Policial() {}
	
	public int getIdPolicial() {
		return idPolicial;
	}
	public void setIdPolicial(int idPolicial) {
		this.idPolicial = idPolicial;
	}
	public String getNomePolicial() {
		return nomePolicial;
	}
	public void setNomePolicial(String nomePolicial) {
		this.nomePolicial = nomePolicial;
	}
	public String getUsuarioPolicial() {
		return usuarioPolicial;
	}
	public void setUsuarioPolicial(String usuarioPolicial) {
		this.usuarioPolicial = usuarioPolicial;
	}
	public String getSenhaPolicial() {
		return senhaPolicial;
	}
	public void setSenhaPolicial(String senhaPolicial) {
		this.senhaPolicial = senhaPolicial;
	}
	
}
